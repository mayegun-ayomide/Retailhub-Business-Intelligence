---- 29. Find the stores where customers made purchases.
SELECT DISTINCT	c.first_name,
				c.last_name,
				s.store_name
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
JOIN stores s
ON s.store_id = o.store_id
GROUP BY	c.last_name,
			c.first_name,
			s.store_name;

----- 30. Create a customer summary showing: - Total customers, Total orders, and Total revenue
SELECT	 COUNT(DISTINCT c.customer_id) AS "Total Customer",
		 COUNT(o.order_id) AS "Total Order",
		 SUM(o.net_amount) AS "Total Revenue"
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id;

---- SECTION F — CASE WHEN Customer Classification

--- 31. Classify customers based on spending: 500000 and above = High Value Customer, 200000 to below 500000 = Medium Value, and Customer Below 200000 = Low Value Customer
SELECT 	c.last_name,
		c.first_name,
		SUM(o.net_amount) AS "Total Spending",
	CASE
		WHEN SUM(o.net_amount) >= 50000 THEN 'High Valued Customer'
		WHEN SUM(o.net_amount)  >= 20000 THEN 'Medium Valued Customer'
		ELSE 'Low Valued Customer'
		END AS "Customer Level"
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY    c.last_name,
			c.first_name;

---- 32. Classify customers based on order frequency:  More than 10 orders = Frequent Buyer, 5 to 10 orders = Regular Buyer, and Below 5 orders = Occasional Buyer
SELECT	c.last_name,
		c.first_name,
		COUNT(o.order_id) AS "Total Order",
	CASE
		WHEN COUNT(o.order_id) >= 10 THEN 'Frequent Buyer'
		WHEN COUNT(o.order_id) >= 5 THEN 'Regular Buyer'
		ELSE 'Occassional Buyer'
		END AS "Buyer Type"
FROM customers c
JOIN orders o 
ON c.customer_id = o.customer_id
GROUP BY 	c.last_name,
			c.first_name;


